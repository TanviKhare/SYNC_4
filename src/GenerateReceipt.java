    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String data = (String)jComboBox1.getSelectedItem();
        int indexSpace = data.indexOf(" ");
        if(indexSpace != -1){
            String query = "SELECT * FROM bills WHERE billid = "+data.substring(0,indexSpace)+";";
            try{
                ResultSet rs = stmt.executeQuery(query);
                if(rs.next())
                {
                    jComboBox1.setEnabled(false);
                    area.append("\n    Bill Id             : "+Integer.toString(rs.getInt("billid"))+"");
                    area.append("\n    Units Consumed      : "+Integer.toString(rs.getInt("units"))+" (KWH)");
                    area.append("\n    Amount              : "+Float.toString(rs.getFloat("amount"))+"");
                    area.append("\n    Month               : "+rs.getString("month")+"");
                    area.append("\n    Year                : "+rs.getString("year")+"");
                    area.append("\n    Generated_on        : "+rs.getTimestamp("generated_on").toString()+"");
                    area.append("\n    Paid_on             : "+rs.getTimestamp("paid_on").toString()+"");
                    area.append("\n");
                    rs.close();
                }
            }catch(Exception e){
                e.printStackTrace();
                System.out.println("Error occured while retreiving your bills " + e);
                JOptionPane.showMessageDialog(null,"Error occured while retreivinf bill data!!");
            }
            
        }
        System.out.println("Changed");
    }//GEN-LAST:event_jComboBox1ActionPerformed

