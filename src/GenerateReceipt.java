    void setDetails(String user,String name,String ucity,String ucontact,String uemail, String meterid,String location,String type,String days)
    {
        String query = "SELECT * FROM bills WHERE userName = '"+user+"' AND paid = true;";
        String entry;
        //String [] data = new String[30];
        //int i;
        try{
            ResultSet rs = stmt.executeQuery(query);
            if(!rs.next())
            {
                JOptionPane.showMessageDialog(null,"You have no paid bills!!");
                setVisible(false);
                
            }else{
                //i = 0;
                
                do
                {
                    entry = Integer.toString(rs.getInt("billid")) + " (" + rs.getString("month") + " " + rs.getString("year") + ")" ;
                    //data[i] = entry;
                    jComboBox1.addItem(entry);
                }while(rs.next());
                
            }
            area.setText("\n                ELECTRICITY BILL                   \n");
            area.append("\n---------------------------------------------------\n\n");
            area.append("\n    Customer Name        : "+name+"");
            area.append("\n    Contact              : "+ucontact+"");
            area.append("\n    City                 : "+ucity+"");
            area.append("\n    Email                : "+uemail+"");
            area.append("\n---------------------------------------------------\n\n");
            area.append("\n    Meter Id             : "+meterid+"");
            area.append("\n    Location             : "+location+"");
            area.append("\n    Meter Type           : "+type+"");
            area.append("\n    Bill Cycle           : "+days+"");
            area.append("\n---------------------------------------------------\n\n");
        }catch(Exception e)
        {
            e.printStackTrace();
            System.out.println("Error occured while retreiving your bills " + e);
            JOptionPane.showMessageDialog(null,"Error occured while retreiving your bills !!");
            setVisible(false);
        }
    }
